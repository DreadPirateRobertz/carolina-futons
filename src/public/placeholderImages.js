/**
 * Build a Wix Media API transform URL from a wix:image:// URI.
 * Used when you need a specific size/quality variant of an image.
 *
 * @param {string} wixImageUri - wix:image://v1/<fileId>/<filename>#...
 * @param {object} opts
 * @param {number} opts.width - Target width
 * @param {number} opts.height - Target height
 * @param {string} [opts.fit='fill'] - 'fill' (crop to fit), 'fit' (contain), 'crop'
 * @param {number} [opts.quality=80] - JPEG quality 1-100
 * @returns {string} Wix static media URL with transforms
 */
export function getTransformedImageUrl(wixImageUri, { width, height, fit = 'fill', quality = 80 }) {
  if (!wixImageUri || !wixImageUri.startsWith('wix:image://')) return wixImageUri;

  // Extract fileId from wix:image://v1/<fileId>/<filename>#...
  const withoutPrefix = wixImageUri.replace('wix:image://v1/', '');
  const fileId = withoutPrefix.split('/')[0].split('#')[0];
  const filename = (withoutPrefix.split('/')[1] || '').split('#')[0] || 'image.jpg';

  return `https://static.wixstatic.com/media/${fileId}/v1/${fit}/w_${width},h_${height},al_c,q_${quality}/${filename}`;
}
